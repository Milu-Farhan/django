function ajaxCall(url, type, data, callback) {
  $.ajax({
    url: url,
    type: type,
    headers: { "X-CSRFToken": csrftoken },
    mode: "same-origin",
    data: data,
  })
    .done(function (response) {
      callback(response);
    })
    .fail(function (response) {
      callback({ response: false });
    });
}
